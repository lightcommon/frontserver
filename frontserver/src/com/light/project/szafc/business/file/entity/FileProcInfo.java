package com.light.project.szafc.business.file.entity;

public class FileProcInfo {
	
	private String businessday;
	
	private String fileName;
	
	private String fileBusiCode;
	
	private String fileProcTime;
	
	private String fileProcStatus;
	
	private String fileType;
	
	private String fileCurrPath;
	
	private String fileExtRep;
	
	private String fileExtDest;
	
	private Integer fileProcTimes;
	
	private String fileDigest;
	
	private String lastTime;
	
	
	
	public String getBusinessday() {
		return businessday;
	}

	public void setBusinessday(String businessday) {
		this.businessday = businessday;
	}

	public String getFileName() {
		return fileName;
	}

	public void setFileName(String fileName) {
		this.fileName = fileName;
	}

	public String getFileBusiCode() {
		return fileBusiCode;
	}

	public void setFileBusiCode(String fileBusiCode) {
		this.fileBusiCode = fileBusiCode;
	}

	public String getFileProcTime() {
		return fileProcTime;
	}

	public void setFileProcTime(String fileProcTime) {
		this.fileProcTime = fileProcTime;
	}

	public String getFileProcStatus() {
		return fileProcStatus;
	}

	public void setFileProcStatus(String fileProcStatus) {
		this.fileProcStatus = fileProcStatus;
	}

	public String getFileType() {
		return fileType;
	}

	public void setFileType(String fileType) {
		this.fileType = fileType;
	}

	public String getFileCurrPath() {
		return fileCurrPath;
	}

	public void setFileCurrPath(String fileCurrPath) {
		this.fileCurrPath = fileCurrPath;
	}

	public String getFileExtRep() {
		return fileExtRep;
	}

	public void setFileExtRep(String fileExtRep) {
		this.fileExtRep = fileExtRep;
	}

	public String getFileExtDest() {
		return fileExtDest;
	}

	public void setFileExtDest(String fileExtDest) {
		this.fileExtDest = fileExtDest;
	}

	public Integer getFileProcTimes() {
		return fileProcTimes;
	}

	public void setFileProcTimes(Integer fileProcTimes) {
		this.fileProcTimes = fileProcTimes;
	}

	public String getFileDigest() {
		return fileDigest;
	}

	public void setFileDigest(String fileDigest) {
		this.fileDigest = fileDigest;
	}

	public String getLastTime() {
		return lastTime;
	}

	public void setLastTime(String lastTime) {
		this.lastTime = lastTime;
	}

	

}
